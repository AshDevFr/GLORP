/**
 * Returns the animation frames for the given species and stage.
 * Falls back to stage 0 of the same species if stage is not found.
 */
export function getAsciiFrames(
  species: Species,
  stage: number,
): readonly string[] {
  const speciesArt = ASCII_ART[species] ?? ASCII_ART.GLORP;
  return speciesArt[stage] ?? speciesArt[0];
}

/**
 * Returns the first frame of ASCII art for the given species/stage.
 * Convenience wrapper for backward compatibility.
 */
export function getAsciiArt(species: Species, stage: number): string {
  return getAsciiFrames(species, stage)[0];
}
