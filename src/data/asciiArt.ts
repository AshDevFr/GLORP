/**
 * Build an array of animation frames from a base art plus
 * per-frame line overrides (for blinks, breathing, etc.).
 */
function frames(
  base: string[],
  ...variants: Array<Array<[number, string]>>
): string[] {
  const result = [base.join("\n")];
  for (const changes of variants) {
    const copy = [...base];
    for (const [idx, line] of changes) {
      copy[idx] = line;
    }
    result.push(copy.join("\n"));
  }
  return result;
}

